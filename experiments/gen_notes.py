"""Генерация experiment_notes.pdf для каждой подпапки экспериментов."""
from fpdf import FPDF
import os

FONT = "/Library/Fonts/Arial Unicode.ttf"
BASE = os.path.dirname(os.path.abspath(__file__))

BLACK = (0, 0, 0)
BLUE = (0, 70, 180)
GRAY = (100, 100, 100)
GREEN = (0, 120, 60)


class PDF(FPDF):
    def __init__(self, title):
        super().__init__()
        self._title = title
        self.add_font("au", "", FONT)
        self.add_font("au", "B", FONT)
        self.set_auto_page_break(auto=True, margin=20)

    def header(self):
        self.set_font("au", "", 7)
        self.set_text_color(*GRAY)
        self.cell(0, 4, self._title, align="L")
        self.cell(0, 4, f"{self.page_no()}", align="R", new_x="LMARGIN", new_y="NEXT")
        self.ln(2)

    def h1(self, txt):
        self.set_font("au", "B", 15)
        self.set_text_color(*BLACK)
        self.ln(2)
        self.multi_cell(0, 8, txt)
        self.ln(2)

    def h2(self, txt):
        self.set_font("au", "B", 12)
        self.set_text_color(*BLUE)
        self.ln(2)
        self.multi_cell(0, 7, txt)
        self.ln(1)

    def p(self, txt):
        self.set_font("au", "", 10)
        self.set_text_color(*BLACK)
        self.multi_cell(0, 5.5, txt)
        self.ln(1.5)

    def code(self, txt):
        self.set_font("au", "", 9)
        self.set_text_color(*GRAY)
        self.multi_cell(0, 5, txt)
        self.ln(1.5)

    def result(self, txt):
        self.set_font("au", "B", 10)
        self.set_text_color(*GREEN)
        self.multi_cell(0, 5.5, txt)
        self.ln(1.5)

    def save(self, path):
        self.output(path)
        print(f"  Saved {path}")


